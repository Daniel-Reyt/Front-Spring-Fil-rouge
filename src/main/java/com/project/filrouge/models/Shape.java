package com.project.filrouge.models;

public class Shape {
    int sides;
    String name;
    int size;

    public int getSides() {
        return sides;
    }

    public String getName() {
        return name;
    }

    public int getSize() {
        return size;
    }

    public int setSides(int sides) {
        this.sides = sides;
        return sides;
    }

    public String setName(String name) {
        this.name = name;
        return name;
    }

    public int setSize(int size) {
        this.size = size;
        return size;
    }

}
